            <div class="content-background">

              <p>The Johari window is a technique that helps people better understand their relationship with themselves and others. It was created by psychologists Joseph Luft (1916–2014) and Harrington Ingham (1916–1995) in 1955, and is used primarily in self-help groups and corporate settings as a heuristic exercise. Luft and Ingham named their model "Johari" using a combination of their first names.</p>
              <h3> <strong>Description</strong> </h3>
              <p>In the exercise, subjects pick a number of adjectives from a list, choosing ones they feel describe their own personality. The subject's peers then get the same list, and each picks an equal number of adjectives that describe the subject. These adjectives are then inserted into a two-by-two grid of four cells.
                The philosopher Charles Handy calls this concept the <i>Johari House with four rooms</i>. Room one is the part of ourselves that we and others see. Room two contains aspects that others see but we are unaware of. Room three is the private space we know but hide from others. Room four is the unconscious part of us that neither ourselves nor others see.</p>
              <h3> <strong>The four quadrants</strong> </h3>
              <ul>
                <li>
                  <p> <strong>Open, or Arena</strong> </p>
                  <p>Adjectives that both the subject and peers select go in this cell (or quadrant) of the grid. These are traits that subject and peers perceive.</p>
                </li>
                <li>
                  <p> <strong>Hidden, or Façade</strong> </p>
                  <p>Adjectives selected by the subject, but not by any of their peers, go in this quadrant. These are things the peers are either unaware of, or that are untrue but for the subject's claim.</p>
                </li>
                <li>
                  <p> <strong>Blind Spot</strong> </p>
                  <p>Adjectives not selected by subjects, but only by their peers go here. These represent what others perceive but the subject does not.</p>
                </li>
                <li>
                  <p> <strong>Unknown</strong> </p>
                  <p>Adjectives that neither subject nor peers selected go here. They represent subject's behaviors or motives that no one participating recognizes—either because they do not apply or because of collective ignorance of these traits.</p>
                </li>
              </ul>
              <h3> <strong>Motivational equivalent</strong> </h3>
              <p>The concept of meta-emotions categorized by basic emotions offers the possibility of a meta-emotional window as a motivational counterpart to the meta-cognitive Johari window.</p>
              <h3> <strong>Therapy</strong> </h3>
              <p>One therapeutic target may be the expansion of the Open (Arena) square at the expense of both the Unknown square and the Blind Spot square, resulting in greater knowledge of oneself, while voluntary disclosure of Private (please use terminology noted in the article - this could be Hidden or Facade) square may result in greater interpersonal intimacy and friendship.</p>
